package br.edu.faculdadedelta.modelo;

public class LaboratorioWalterPaulo {
	private Long id;
	private int numero;
	private String descricao;
	public LaboratorioWalterPaulo() {
		super();
	}
	public LaboratorioWalterPaulo(Long id, int numero, String descricao) {
		super();
		this.id = id;
		this.numero = numero;
		this.descricao = descricao;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		LaboratorioWalterPaulo other = (LaboratorioWalterPaulo) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
}
